#ifndef _E201_FLASH_H__ 
#define _E201_FLASH_H__

#include "hwfcl.h"

#define FLASH_SECTOR_BYTES		4096   //FLASH的一个Sector的字节数

////////////////////////////////////////////////////////////////////////////////
//描述: 
//     初始化Flash驱动程序,系统初始化时只调用一次
//     返回初始化是否成功
//     在调用下面的接口之前必须成功调用过此初始化函数
BOOL   InitFlashDrv();

////////////////////////////////////////////////////////////////////////////////
//描述:
//     擦除FALSH的一个Sector
//     返回是否成功
BOOL   FlashEraseSector(UINT16 *lpwDst);

////////////////////////////////////////////////////////////////////////////////
//描述:
//     在指定地址写入一个字(双字节)
//     指定地址lpwDst必须是偶地址
//     返回是否成功
BOOL   FlashWriteWord(UINT16  *lpwDst, UINT16 wSrc);

////////////////////////////////////////////////////////////////////////////////
//描述:
//     把指定地址开始的dwSrcWords个字写到日标Flash地址lpwDst中去
//     lpwDst和lpwSrc都必须是偶地址
//     返回成功写入的字数
BOOL FlashWrite(UINT16 *lpwDst, UINT16 *lpwSrc, UINT32 dwSrcWords);

#endif 
